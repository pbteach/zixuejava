/*
算术运算符：+,-,*,/,%,++,--
*/
public class Operator1 
{
	public static void main(String[] args) 
	{
		//+ 加、正号、字符串连接
		int a = 1;
		int b = 2;
		String s1 = "攀博网";
		String s2 = "自学编程";
		//s1和s2字符串拼接
		String s3 = s1 + s2;
		System.out.println(a+b);
		System.out.println(+b);
		System.out.println(s3);
		
	}

	
}
