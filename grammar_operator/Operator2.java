/*
赋值运算符：=、+=、-=、*=、/=、%=
*/
public class Operator2 
{
	public static void main(String[] args) 
	{

		//定义两个变量
		int a=1;
		int b=2;
		//+=赋值
		b+=a;  
		
		//定义两个变量a和b
		int c,d; 
		//为a、b赋值
		c = d = 100;
		System.out.println(c); 
		System.out.println(d);

		//定义一个变量
		int e = 100;
		
		e += 200;
		System.out.println(e);

	}

	
}
